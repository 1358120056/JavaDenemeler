package com.company;

public class IndexDeger {

    private int baslangicAdresi;
    private int uzunluk;

    public IndexDeger(int baslangicAdresi, int uzunluk) {
        this.baslangicAdresi = baslangicAdresi;
        this.uzunluk = uzunluk;
    }

    public int getBaslangicAdresi() {
        return baslangicAdresi;
    }

    public void setBaslangicAdresi(int baslangicAdresi) {
        this.baslangicAdresi = baslangicAdresi;
    }

    public int getUzunluk() {
        return uzunluk;
    }

    public void setUzunluk(int uzunluk) {
        this.uzunluk = uzunluk;
    }
}

